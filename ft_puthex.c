#include "ft_printf.h"

int	ft_puthex(unsigned int n, char format)
{
	char	*base;

	if (format == 'x')
		base = "0123456789abcdef";
	else
		base = "0123456789ABCDEF";
	return (ft_putnbr_base((unsigned long)n, base));
}
